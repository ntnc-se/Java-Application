    @Override
    public void run() {
        while (true) {
            if (lblWelcome.getForeground() == Color.BLACK) {
                lblWelcome.setForeground(Color.red);
            } else if (lblWelcome.getForeground() == Color.red) {
                lblWelcome.setForeground(Color.orange);
            } else {
                lblWelcome.setForeground(Color.black);
            }
            try {
                Thread.sleep(500);
            } catch (InterruptedException ex) {
                Logger.getLogger(MenuUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
