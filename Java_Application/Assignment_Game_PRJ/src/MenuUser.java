    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            Player p = new Player();
            p.setName(txtPlayerName.getText());
            p.setScore(screen.getScore());
            if (radEasy.isSelected()) {
                p.setLevel("Easy");
            }
            if (radHard.isSelected()) {
                p.setLevel("Hard");
            }
            if (radMedium.isSelected()) {
                p.setLevel("Medium");
            }
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDateTime now = LocalDateTime.now();
            p.setDatePlay(dtf.format(now));
            stub = (IPlayer) Naming.lookup("rmi://localhost:9999/ntnc");
            stub.insert(p);
        } catch (NotBoundException ex) {
            Logger.getLogger(MenuUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(MenuUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(MenuUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtPlayerName.setText("");
        JOptionPane.showMessageDialog(rootPane, "Your data have been recorded !");
    }//GEN-LAST:event_btnSaveActionPerformed

